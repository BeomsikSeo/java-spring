package 확인문제;

public class 문제풀이2 {

	public static void main(String[] args) {
		double num1 = 30;
		double num2 = 40;
		
		// 두 수를 더한 값은 얼마?
		double result1 = num1 + num2;
		System.out.println("두 수를 더한 값은 " + result1);
		
		// num1에서 num2를 나누면 얼마?
		double result2 = num1 / num2;
		System.out.println("두 수를 나눈 값은 " + result2);
		
		// num1에서 num2를 나누면 나머지는 얼마?
		double result3 = num1 % num2;
		System.out.println("나눈 나머지는 " + result3);

	}

}
